				<p>The Project Inventory includes two tools to help identify restoration projects:</p>
				<p>The Project Inventory Search Tool allows users to identify restoration projects 
					based on a particular topic or combination of two topics. For example, a
					 user can search the database for projects of a particular habitat type 
					 (such as coastal wetland), or a user could narrow the search to projects 
					 of a particular habitat type (such as coastal wetland) within a particular 
					 geographic area (such as town or county).
						The Project Inventory Map allows users to directly access information from the 
					database pertaining to specific projects by clicking on mapped project sites.
 					Please be patient while map loads slowly into your browser.</p>
