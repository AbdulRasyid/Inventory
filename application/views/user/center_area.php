				<p>The majority of the projects in the Project Inventory are salt 
					marsh restoration, eelgrass restoration, or anadromous fish run (
					Alewife or Blueback Herring) restoration projects. The Project 
					Inventory contains information about the restoration site 
					including where the site is located, the organizations 
					involved with the site, the type of impact to the resources
					 and contact information for the project leader. Other types
					  of information the user will see include the following: 
				</p>
