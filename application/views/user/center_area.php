				<li class="desc_head">Project Description</li>
				<p>The Project Inventory is a database designed to help users 
					identify restoration opportunities and completed projects 
					in Rhode Island watersheds. The projects represent several 
					collaborative efforts to identify restoration activities in 
					the state. The projects are classed as "potential" (unfunded),
					 "ongoing" (funded but not yet completed), or "complete." These
					  efforts include Save The Bay's volunteer salt marsh evaluations, 
					  the Narragansett Bay Estuary Program's Habitat Restoration Charrette 
					  (PDF file), and the efforts of the Coastal Resources 
					  Management Council to identify restoration opportunities 
					  with the Army Corps of Engineers and through the regulatory
					   permit process.</p>
